public class Operators {
    public static void main(String[] args) {
        
        int n1 = 5;
        int n2 = 6;

        int result = n1+n2;
        System.out.println(result);
    
        //we can also increment and decrement the nnumbers by itself.

        int num = 5;
        num+=2; //it increment the num by num itself it looks like num = num+2;

        num -=2; //it decrement the num by num itself it looks like num = num-2;


        ++num;  //post increment num++;
        num++;  //pre increment ++num;


        System.out.println(num);
    
    }
    
}
